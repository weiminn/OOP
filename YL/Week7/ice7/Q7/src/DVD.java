public class DVD {
    // instance variables/attributes
    private int id;
    private String title;

    // specific constructor
    public DVD(int id, String title) {
        this.id = id;
        this.title = title;
    }

    // a getter method
    public int getId() {
        return id;
    }

    // yet another getter method
    public String getTitle() {
        return title;
    }

}